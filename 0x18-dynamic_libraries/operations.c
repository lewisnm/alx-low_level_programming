#include <stdio.h>

/**
 * add - adds two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - multiplies two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - divides two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - gives the remainder of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int mod(int a, int b)
{
        return (a % b);
}
